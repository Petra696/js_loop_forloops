
for (var i = 0; i <= 20; i++) {
    if (i === 0) {
        console.log(i + " is even");
    }
    else if (i % 2 === 0) {
        console.log(i + " is even");
    }
    else {
        console.log(i + " is odd");
    }
}

  // Expected output:
  // 1 is odd 
  // 2 is even 
  // 3 is odd 
  // 4 is even 
  // ...

